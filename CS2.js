//Memasukan hari bulan dan tahun dengan angka
//Mengubah bulan(angka) menjadi nama bulan dengan conditional switch case

let hari = '1';
let bulan = 5;
let tahun = '1945';

switch(bulan) {
    case 1:
        bulan = "Januari";
        break;
    case 2:
        bulan = "Februari";
        break;
    case 3:
        bulan = "Maret";
    case 4:
        bulan = "April";
        break;
    case 5:
        bulan = "Mei";
        break;
    case 6:
        bulan = "Juni";
        break;
    case 7:
        bulan = "Juli";
        break;
    case 8:
        bulan = "Agustus";
        break;
    case 9:
        bulan = "September";
        break;
    case 10:
        bulan = "Oktober";
        break;
    case 11:
        bulan = "November";
        break;
    case 12:
        bulan = "Desember";
        break;
}
waktu = hari + " " + bulan + " " + tahun;

console.log(waktu);